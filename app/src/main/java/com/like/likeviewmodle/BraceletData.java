class BraceletData {
    private String temp, step;

    public BraceletData(String temp, String step) {
        this.temp = temp;
        this.step = step;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getStep() {
        return step;
    }

    public void setStep(String step) {
        this.step = step;
    }
}
